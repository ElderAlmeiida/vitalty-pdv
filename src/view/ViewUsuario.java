    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        modelUsuario = new ModelUsuario();
        modelUsuario.setUsuNome(jtNomeUsuario.getText());
        modelUsuario.setUsuLogin(jtLoginUsuario.getText());
        modelUsuario.setUsuSenha(String.valueOf(jpSenha.getPassword()));
        if (controllerUsuario.salvarUsuarioController(modelUsuario)){
            //cadastra usuario
            JOptionPane.showMessageDialog(this, "Usuario cadastro com sucesso");
            //limpar
            limparFormulario();
            carregarUsuario();
            
        } else {
            //nao cadastrou
            JOptionPane.showMessageDialog(this, "NÃ£o foi possivel cadastrar","ERROR",JOptionPane.ERROR_MESSAGE);
        }
               
    }//GEN-LAST:event_jbSalvarActionPerformed
       /**
             * limpa formulario user
             */
    private void limparFormulario(){
          jtCodUsuario.setText("");
          jtNomeUsuario.setText("");
          jtLoginUsuario.setText("");
          jpSenha.setText("");
        }
        private void carregarUsuario(){
            listaUsuario = controllerUsuario.getListaUsuariosController();
            DefaultTableModel modelo = (DefaultTableModel) jtlTabelaUsuario.getModel();
            modelo.setNumRows(0);
            
            for (int i = 0;i<listaUsuario.size();i++) {
             modelo.addRow(new Object [] {
                 listaUsuario.get(i).getUsuId(),
                 listaUsuario.get(i).getUsuNome(),
                 listaUsuario.get(i).getUsuSenha()
             }) ;
        }
            
        }
